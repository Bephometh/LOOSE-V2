    <h1> LOOSE </h1>
    <p>
        We are a community of climbers who want to show that climbing doesn't have to be uptight; it's a sport where we can just stay loose and free. We try to show this by just having fun climbing, developing, and sharing with the community. We aim to create a space where people feel comfortable exploring climbing without pressure, expectations, or rigid definitions of what a climber should be.

        One of the ways we try to reach out is by spreading outdoor climbing activities around the Bogor area. Outdoor climbing, particularly bouldering, is still not widely explored in the local scene, so we’re trying to give it more space while continuing to appreciate how sport climbing has grown.
